namespace Grower
{
    /// <summary>
    /// Enum representing the side of the object where the collision occurred.
    /// This enum is used to specify which side of the object was involved in the collision.
    /// </summary>
    public enum CollisionSide
    {
        /// <summary>
        /// The top side of the object.
        /// </summary>
        Top,

        /// <summary>
        /// The bottom side of the object.
        /// </summary>
        Bottom,

        /// <summary>
        /// The left side of the object.
        /// </summary>
        Left,

        /// <summary>
        /// The right side of the object.
        /// </summary>
        Right
    }
}