namespace Grower
{
    /// <summary>
    /// Represents the cardinal directions for swipe detection.
    /// </summary>
    public enum CardinalDirection
    {
        /// <summary>
        /// No direction detected.
        /// </summary>
        None,

        /// <summary>
        /// Swipe in the upward direction.
        /// </summary>
        Up,

        /// <summary>
        /// Swipe in the downward direction.
        /// </summary>
        Down,

        /// <summary>
        /// Swipe in the leftward direction.
        /// </summary>
        Left,

        /// <summary>
        /// Swipe in the rightward direction.
        /// </summary>
        Right
    }
}